/*#############################################
#######Ein einfaches Beispiel für Variablen####
#############################################*/
#include <iostream>

using namespace std;

int main()
{
    // Vereinbarung einer Variable
    int wert, wert1, summe ;
    // 1. Zuweisung
    wert = 100 + 20;
    // 1. Ausgabe
    cout << wert << endl;
    // 2. Zuweisung
    wert1 = 100 + 30;
    // 3. Ausgabe
    cout << wert1<< endl;
    summe = wert + wert1;
    cout << summe << endl;
    return 0;
}
