#include <iostream>
#include <cstdio>

using namespace std;

int main()
{
    int zahl =100;
    // die Schleife
    do {
                        //Schleifenkopf
            cout << zahl << "\n";
              zahl--;  //Schleifenkˆrper
                                                   //Schleifenkˆrper
    }

    while (zahl >=80);          //Schleifenfuﬂ


    return 0;
}
